public class rectangleshape {
    public static void main(String[] args) {

        System.out.println("Rectangle Kebawah");
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 7; j++) {
                System.out.print("*");
            }
            System.out.print("\n");
        }

        System.out.println("Rectangle Kesamping");
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 25; j++) {
                System.out.print("*");
            }
            System.out.print("\n");
        }
    }
}
